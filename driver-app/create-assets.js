const fs = require('fs');
const path = require('path');

// Create a simple red square PNG as placeholder
const createPlaceholderPNG = (width, height, filename) => {
  // This is a minimal red PNG
  const png = Buffer.from([
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG signature
    0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52, // IHDR chunk
    0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, // 1x1 pixel
    0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53,
    0xDE, 0x00, 0x00, 0x00, 0x0C, 0x49, 0x44, 0x41,
    0x54, 0x08, 0x99, 0x63, 0xF8, 0xCF, 0xC0, 0x00,
    0x00, 0x00, 0x03, 0x00, 0x01, 0x5B, 0x84, 0xC6,
    0x60, 0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4E,
    0x44, 0xAE, 0x42, 0x60, 0x82 // IEND chunk
  ]);
  
  fs.writeFileSync(path.join(__dirname, 'assets', filename), png);
  console.log(`Created ${filename}`);
};

// Create placeholder images
createPlaceholderPNG(1024, 1024, 'icon.png');
createPlaceholderPNG(1024, 1024, 'adaptive-icon.png');
